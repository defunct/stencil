package com.goodworkalan.stencil;

/**
 * A comment in an XML document.
 *
 * @author Alan Gutierrez
 */
class Comment {
    /** The comment text. */
    public final String text;

    /**
     * Create a comment with the given comment text.
     * 
     * @param text
     *            The comment text.
     */
    public Comment(String text) {
        this.text = text;
    }
}
